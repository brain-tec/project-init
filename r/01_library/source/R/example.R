#' Adds the number 42 to the specified number.
#'
#' Here we could give some more information about this
#' extraordinary function, but we think it's pretty self-explanatory.
#'
#' @param num The number to add 42 to.
#'
#' @return The specified number plus 42 added to it.
#'
#' @details
#' This is pretty self-explanatory.
#'
#' @examples
#' \dontrun{
#' # This is a comment
#' x <- addFortyTwo(5)
#' }
#'
#' @export
#'
addFortyTwo <- function(num){
  return(num + 42)
}

